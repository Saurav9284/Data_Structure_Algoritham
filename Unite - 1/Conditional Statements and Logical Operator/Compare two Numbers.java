public static void compareTwoNumbers(int num1,int num2){
      
      if(num1>num2)
      {
          System.out.println("true");
      }
      else
      {
          System.out.println("false");
      }
      if(num1<num2)
      {
          System.out.println("true");
      }
      else
      {
          System.out.println("false");
      }
      if(num1==num2)
      {
          System.out.println("true");
      }
      else
      {
          System.out.println("false");
      }
      
  }
