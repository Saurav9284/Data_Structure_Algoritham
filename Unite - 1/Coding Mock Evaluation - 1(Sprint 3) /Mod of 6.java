public static void modOf6(int num){
    
     if(num%6==0)
     {
         System.out.println("zero");
     }
     if(num%6==1)
     {
         System.out.println("one");
     }
     if(num%6==2)
     {
         System.out.println("two");
     }
     if(num%6==3)
     {
         System.out.println("three");
     }
     if(num%6==4)
     {
         System.out.println("four");
     }
     if(num%6==5)
     {
         System.out.println("five");
     }
  }
